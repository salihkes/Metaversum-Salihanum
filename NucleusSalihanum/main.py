# ============================================================================
# Web-to-Game Single Sign-On (SSO) Token System
# ============================================================================
# Tokens are generated by the website when a logged-in user wants to play
# the Godot game. The game (HTML5 export) receives the token via JavaScript
# and sends it to authenticate without needing to re-enter credentials.
#
# Token format: base64(username:timestamp:signature)
# The signature is HMAC-SHA256 of "username:timestamp" with the shared secret.
# This allows the website to generate tokens and the game server to validate
# them without needing shared storage.
# ============================================================================
import hmac
import hashlib
from constants import SSO_SECRET_KEY, SSO_TOKEN_EXPIRY_SECONDS

# Track used tokens to prevent replay attacks (tokens are one-time use)
used_tokens = set()

def generate_sso_token(username):
    """Generate a signed SSO token for web-to-game authentication.
    
    Token format: base64(username:timestamp:signature)
    """
    timestamp = str(int(time.time()))
    message = f"{username}:{timestamp}"
    signature = hmac.new(
        SSO_SECRET_KEY.encode(), 
        message.encode(), 
        hashlib.sha256
    ).hexdigest()
    
    token_data = f"{username}:{timestamp}:{signature}"
    token = base64.b64encode(token_data.encode()).decode()
    
    print(f"[SSO] Generated token for {username} (expires in {SSO_TOKEN_EXPIRY_SECONDS}s)")
    return token

def validate_sso_token(token):
    """Validate an SSO token. Returns username if valid, None otherwise.
    
    Validates:
    1. Token format is correct
    2. Signature matches (token wasn't tampered with)
    3. Token hasn't expired
    4. Token hasn't been used before (one-time use)
    """
    try:
        # Decode the token
        token_data = base64.b64decode(token).decode()
        parts = token_data.split(':')
        
        if len(parts) != 3:
            print(f"[SSO] Invalid token format")
            return None
        
        username, timestamp_str, provided_signature = parts
        
        # Check if token was already used
        if token in used_tokens:
            print(f"[SSO] Token already used for {username}")
            return None
        
        # Verify signature
        message = f"{username}:{timestamp_str}"
        expected_signature = hmac.new(
            SSO_SECRET_KEY.encode(),
            message.encode(),
            hashlib.sha256
        ).hexdigest()
        
        if not hmac.compare_digest(provided_signature, expected_signature):
            print(f"[SSO] Invalid signature for token")
            return None
        
        # Check expiry
        timestamp = int(timestamp_str)
        now = int(time.time())
        
        if now - timestamp > SSO_TOKEN_EXPIRY_SECONDS:
            print(f"[SSO] Token expired for {username}")
            return None
        
        # Mark token as used (prevent replay attacks)
        used_tokens.add(token)
        
        # Clean up old used tokens periodically (keep last 1000)
        if len(used_tokens) > 1000:
            # Remove oldest entries (simple approach - just clear when too many)
            used_tokens.clear()
        
        print(f"[SSO] Token validated for {username}")
        return username
        
    except Exception as e:
        print(f"[SSO] Token validation error: {e}")
        return None

